public class Servicio {
	String paquete, code, descripcion, incluye, excluye, tipo_precio, precio_sugeridoLM, precio_1h, precio_nexth, tips;

	public Servicio() {
		super();
	}

	public String getPaquete() {
		return paquete;
	}

	public void setPaquete(String paquete) {
		this.paquete = paquete;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getIncluye() {
		return incluye;
	}

	public void setIncluye(String incluye) {
		this.incluye = incluye;
	}

	public String getExcluye() {
		return excluye;
	}

	public void setExcluye(String excluye) {
		this.excluye = excluye;
	}

	public String getTipo_precio() {
		return tipo_precio;
	}

	public void setTipo_precio(String tipo_precio) {
		this.tipo_precio = tipo_precio;
	}

	public String getPrecio_sugeridoLM() {
		return precio_sugeridoLM;
	}

	public void setPrecio_sugeridoLM(String precio_sugeridoLM) {
		this.precio_sugeridoLM = precio_sugeridoLM;
	}

	public String getPrecio_1h() {
		return precio_1h;
	}

	public void setPrecio_1h(String precio_1h) {
		this.precio_1h = precio_1h;
	}

	public String getPrecio_nexth() {
		return precio_nexth;
	}

	public void setPrecio_nexth(String precio_nexth) {
		this.precio_nexth = precio_nexth;
	}

	public String getTips() {
		return tips;
	}

	public void setTips(String tips) {
		this.tips = tips;
	}

}